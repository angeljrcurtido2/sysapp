import React from 'react';
import {
  Modal,
  View,
  Pressable,
  Platform,
  KeyboardAvoidingView,
} from 'react-native';
import CrearCompra from '../CrearCompra';

interface ModalCrearCompraProps {
  isOpen: boolean;
  onClose: () => void;
  onSuccess?: () => void;
}

const ModalCrearCompra: React.FC<ModalCrearCompraProps> = ({
  isOpen,
  onClose,
  onSuccess
}) => {
  if (!isOpen) return null;

  return (
    <Modal
      visible={isOpen}
      transparent
      animationType="fade"
      statusBarTranslucent
      onRequestClose={onClose}
      presentationStyle={Platform.OS === "ios" ? "overFullScreen" : undefined}
    >
      <View className="flex-1 items-center justify-center bg-black/50 p-4">
        {/* Backdrop - solo toca el fondo */}
        <Pressable
          onPress={onClose}
          className="absolute inset-0"
        />

        <KeyboardAvoidingView
          behavior={Platform.select({ ios: "padding", android: undefined })}
          className="w-full"
        >
          <View
            className="w-full max-w-[700px] self-center rounded-2xl bg-white p-6"
            
          >
            <CrearCompra onSuccess={onSuccess} onClose={onClose} />
          </View>
        </KeyboardAvoidingView>
      </View>
    </Modal>
  );
};

export default ModalCrearCompra;